  def self.cuteness text
    text = text.to_s.strip.downcase

    if text.empty?
      key = IMAGES.keys.sample
      return IMAGES[key].sample
    else
      IMAGES.each do |type, images|
        if text.include?(type)
          return images.sample
        end
      end
    end

    return "Uh noes, we don't know about '#{text}' yet. Try #{IMAGES.keys.join(' or ')}"
  end
